using Content.Shared.Changeling;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.CustomControls;
using Robust.Client.UserInterface.XAML;

namespace Content.Client.Changeling.Ui;

[GenerateTypedNameReferences]
public sealed partial class TransformationsMenu : DefaultWindow
{
    public event Action<string>? OnTransformAction;

    public TransformationsMenu()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);
    }

    public void UpdateState(TransformationsBoundUserInterfaceState state)
    {
        Names.Clear();
        foreach (var name in state.Names)
        {
            Names.AddItem(name).OnSelected += _ => {
                OnTransformAction?.Invoke(name);
            };
        }
    }
}
